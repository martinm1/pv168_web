-- schema-javadb.sql
-- DDL commands for JavaDB/Derby
CREATE TABLE "MISSION" (
    "ID" BIGINT NOT NULL PRIMARY KEY GENERATED ALWAYS AS IDENTITY,
    "DANGER" INTEGER NOT NULL,
    "ASSIGNMENT" VARCHAR(2048) NOT NULL
);

CREATE TABLE "AGENT" (
    "ID" BIGINT NOT NULL PRIMARY KEY GENERATED ALWAYS AS IDENTITY,
    "MISSIONID" BIGINT REFERENCES MISSION (ID),
    "NAME" VARCHAR(255) NOT NULL,
    "WORKINGSINCE" VARCHAR(255) NOT NULL,
    "COMPROMISED" BOOLEAN
);